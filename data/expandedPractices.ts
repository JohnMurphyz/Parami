/**
 * Get all expanded practices for a specific Parami
 */
export function getExpandedPractices(paramiId: number): Practice[] {
  return EXPANDED_PRACTICES[paramiId] || [];
}

/**
 * Get a specific practice by ID from the expanded pool
 */
export function getExpandedPracticeById(practiceId: string): Practice | undefined {
  for (const practices of Object.values(EXPANDED_PRACTICES)) {
    const practice = practices.find(p => p.id === practiceId);
    if (practice) return practice;
  }
  return undefined;
}
